
public class Uppgift3 {
	 public static void main(String[] args) {
	        int[] array = {1, 3, 7, 9, 11};
	    	
	    	  for(int i = 0; i<=4; i++){
	        	System.out.print(array[i] + " ");
	    	  }
	    	  
	    	  System.out.println();
	    	  
	    	  for(int i = 4; i>=0; i--){
		        	System.out.print(array[i] + " ");
		    	  }
	    }
}
